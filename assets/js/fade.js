jQuery(document).ready(function() {
	jQuery(window).scroll(function() {
		if (jQuery(this).scrollTop() > 1300) {
			jQuery('.icon1').fadeIn(500);
		} else {
			jQuery('.icon1').fadeOut(500);
		}
	});
});

